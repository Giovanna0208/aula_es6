//igual (==)
console.log(3 == '3');

//não igual (!=)
console.log(3 != 2);

//estritamente igual (===)
console.log(3 === '3');

//estritamente não igual (!==)
console.log(3 !== '3');

//maior que (>)
console.log(12 > 11);

//maior ou igual que (>=)
console.log(12 >= 12);

//menor que (<)
console.log(11 < 12);

//menor ou igual (<=)
console.log(12 <= 12);